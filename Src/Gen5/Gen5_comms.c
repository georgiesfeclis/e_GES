/* ----------------------------------------------------------------------------
 * Implements
 */
#include <Gen5/Gen5_comms.h>
#include <Gen5/rf_commands.h>
/* ----------------------------------------------------------------------------
 * Private types
 */



static t_spiTxFlag spiTxFlag = TxFlagReset;

static t_GEN5_RxData GEN5_RxData;
/* ----------------------------------------------------------------------------
 * Private defines
 */

/* ----------------------------------------------------------------------------
 * Private macros
 */

/* ----------------------------------------------------------------------------
 * Private variables 
 */
static uint16_t rxCount = 0;
/* ----------------------------------------------------------------------------
 * Private functions
 */

/* ----------------------------------------------------------------------------
 * Public variables
 */

/* ----------------------------------------------------------------------------
 * Public functions
 */
/* ----------------------------------------------------------------------------*/
void Increment_RxCount(void)
{
	rxCount++;
}
/* ----------------------------------------------------------------------------*/

/* ----------------------------------------------------------------------------*/
uint16_t Get_RxCount(void)
{
	return rxCount;
}
/* ----------------------------------------------------------------------------*/

/* ----------------------------------------------------------------------------*/
void Reset_RxCount(void)
{
	rxCount = 0;
}
/* ----------------------------------------------------------------------------*/

/* ----------------------------------------------------------------------------*/
void Update_RfCommand_Name(uint8_t cmd_name)
{
	GEN5_RxData.cmd_name = cmd_name;
}
/* ----------------------------------------------------------------------------*/

/* ----------------------------------------------------------------------------*/
void Update_RfCommand_Size(uint8_t cmd_size)
{
	GEN5_RxData.cmd_size = cmd_size;
}
/* ----------------------------------------------------------------------------*/

/* ----------------------------------------------------------------------------*/
uint8_t Get_RfCommand_Name(void)
{
	return GEN5_RxData.cmd_name;
}
/* ----------------------------------------------------------------------------*/

/* ----------------------------------------------------------------------------*/
uint8_t Get_RfCommand_Size(void)
{
	return GEN5_RxData.cmd_size;
}
/* ----------------------------------------------------------------------------*/

/* ----------------------------------------------------------------------------*/
void Set_SpiTx_Flag(void)
{
	spiTxFlag = TxFlagSet;
}
/* ----------------------------------------------------------------------------*/

/* ----------------------------------------------------------------------------*/
void Reset_SpiTx_Flag(void)
{
	spiTxFlag = TxFlagReset;
}
/* ----------------------------------------------------------------------------*/

/* ----------------------------------------------------------------------------*/
t_spiTxFlag Get_SpiTx_Flag(void)
{
	return spiTxFlag;
}
/* ----------------------------------------------------------------------------*/

/* ----------------------------------------------------------------------------*/
void Configure_RF_Events(void)
{
	RfEvent0.system_ev_byte = 0x04;
	RfEvent1.event_ev_byte  = 0x07;
	RfEvent2.power_ev_byte  = 0x5A;
	RfEvent3.config_ev_byte = 0xA5;
}
